import { Domain } from '../generated/prisma'

/**
 * 项目状态追踪文档 Prompt 模板
 * 基于 Task 文档生成项目状态追踪文件
 * 用于记录开发进度，指导下一步开发
 */

export function getContextStatePromptTemplate(domain: Domain): string {
  const domainLabel = domain === 'SE' ? '软件工程' : '大数据';

  return `
# ${domainLabel}项目状态追踪文档模板

## 项目概述
简要描述项目名称、目标和当前整体进度。

## 任务完成状态
根据任务清单，列出每个任务的完成状态：

\`\`\`markdown
| 任务编号 | 任务名称 | 状态 | 完成时间 | 备注 |
|---------|---------|------|---------|------|
| T-01 | xxx | PENDING | - | - |
| T-02 | xxx | PENDING | - | - |
\`\`\`

状态值：
- PENDING：未开始
- IN_PROGRESS：进行中
- COMPLETED：已完成
- BLOCKED：阻塞中

## 当前进度
- 已完成任务数 / 总任务数
- 完成百分比
- 当前正在进行中的任务

## 下一步行动
根据任务依赖关系，列出接下来应该执行的任务（按优先级排序）。

## 遇到的问题
记录开发过程中遇到的问题和解决方案。

## 变更记录
记录每次状态更新的时间和内容。

---

**篇幅要求**：
- 全文目标 2500-3500 中文字符
- 任务状态表格必须完整列出所有任务
- 下一步行动至少列出 3 个具体任务

**输出要求**：
- 严格按照指定结构输出，使用 Markdown 格式
- 表格格式必须规范
- 下一步行动必须具体可执行
- 这是一个需要持续更新的文档，每次任务完成后都要更新此文件
- 内容需贴合${domainLabel}领域特点
`;
}
