import { Domain } from '../generated/prisma'

/**
 * AI 编码规则文档 Prompt 模板
 * 基于所有前置文档生成 AGENTS.md，用于指导 AI Coding 工具
 */

export function getAgentsPromptTemplate(domain: Domain): string {
  const domainLabel = domain === 'SE' ? '软件工程' : '大数据';

  return `
# AGENTS.md — ${domainLabel}项目 AI 编码规则文档

## 最高优先规则
1. 在开始任何编码、拆任务、写实现或调用外部 AI 工具之前，必须先审核全部 7 份项目文档：PRD.md、Frontend.md、Backend.md、API.md、task.md、context_state.md、AGENTS.md。
2. 只要任意文档中存在“审核修订记录”或“审核修订”内容，必须先依据这些修订记录重新编排、整合并规划相关文档内容，确保文档结构、术语、接口、任务顺序和执行约束已经吸收修订结果。
3. 完成上述整理前，不得直接进入编码阶段，也不得忽略修订记录继续实现。
4. 当用户在 AI 编程工具中输入“了解项目规则，查看 AGENTS.md 文档”时，必须立即先执行本规则，再开始后续编码引导。

## 项目概述
从 PRD 中提取项目背景、目标和核心价值定位。

## 技术栈
从前端和后端文档中提取完整的技术栈清单：
- 前端框架与库
- 后端框架与中间件
- 数据库与缓存
- 构建工具
- 测试框架

## 项目结构
描述项目的目录结构和文件组织方式。

## API 契约摘要
从 API 文档中提取关键接口信息：
- 认证方式
- 基础 URL
- 核心接口列表

## 任务执行指南
从 Task 文档中提取：
- 任务执行顺序
- 任务依赖关系
- 每个任务的验收标准

## 开发规则

### 代码规范
- 代码风格要求
- 命名规范
- 注释要求

### 安全要求
- 输入验证
- 认证授权
- 数据加密

### 测试要求
- 单元测试覆盖
- 集成测试范围

## ContextState 更新规则
**重要：每次完成一个任务后，必须按以下步骤更新 context_state.md 文件：**

1. 将已完成任务的状态从 PENDING 更新为 COMPLETED
2. 记录完成时间
3. 更新已完成任务数 / 总任务数
4. 根据任务依赖关系，确定下一个应该执行的任务
5. 更新"下一步行动"部分
6. 如有问题，记录到"遇到的问题"部分

**更新格式示例：**
\`\`\`markdown
## 变更记录
- [YYYY-MM-DD HH:MM] 完成 T-01: xxx，下一步执行 T-02: xxx
\`\`\`

## 文档引用
本项目包含以下文档：
1. PRD.md — 产品需求文档
2. Frontend.md — 前端技术文档
3. Backend.md — 后端技术文档
4. API.md — API 接口契约文档
5. task.md — 开发任务清单
6. context_state.md — 项目状态追踪文档（每次完成任务后更新此文件）
7. AGENTS.md — 本文件（AI 编码规则）

## 执行流程
1. 首先执行“最高优先规则”，检查全部 7 份文档中的审核修订记录并先完成文档重整
2. 阅读 PRD.md 了解项目需求
3. 阅读 Frontend.md 和 Backend.md 了解技术方案
4. 阅读 API.md 了解接口契约
5. 阅读 task.md 了解任务清单
6. 阅读 context_state.md 了解当前进度
7. 按照任务清单顺序执行开发任务
8. 每完成一个任务，立即更新 context_state.md

---

**篇幅要求**：
- 全文目标 4000-5000 中文字符，内容准确详实
- 各部分内容需准确且有足够的可操作细节

**输出要求**：
- 严格按照指定结构输出，使用 Markdown 格式
- 这是 AI Coding 工具的主要参考文档，内容必须准确完整
- ContextState 更新规则部分必须清晰明确
- 内容需贴合${domainLabel}领域特点
`;
}
