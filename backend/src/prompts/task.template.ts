import { Domain } from '../generated/prisma'

/**
 * 开发任务清单 Prompt 模板
 * 基于 PRD、前端、后端、API 文档生成可执行的任务清单
 */

export function getTaskPromptTemplate(domain: Domain): string {
  const domainLabel = domain === 'SE' ? '软件工程' : '大数据';

  return `
# ${domainLabel}项目开发任务清单模板

## 任务清单概述
根据项目需求文档、技术文档和 API 契约，拆解为可执行的开发任务。
每个任务必须独立完成，有明确的验收标准。

## 任务结构要求
每个任务必须包含以下字段：

### 任务模板
\`\`\`markdown
## T-XX: [任务名称]

**类型**: [后端/前端/数据库/测试/部署]
**优先级**: [P0-P3]
**依赖**: [无 / T-XX, T-XX]
**预估工作量**: [小/中/大]
**描述**: [任务详细描述]
**验收标准**:
- [ ] [验收项 1]
- [ ] [验收项 2]
- [ ] [验收项 3]
\`\`\`

## 任务分组

### 第一阶段：基础设施（P0）
- 项目初始化
- 数据库设计与迁移
- 认证授权模块
- 基础 API 框架

### 第二阶段：核心功能（P0-P1）
根据 PRD 中的核心功能模块，拆解为具体开发任务。
每个功能模块拆分为后端 API 实现和前端页面实现。

### 第三阶段：扩展功能（P1-P2）
根据 PRD 中的次要功能，拆解任务。

### 第四阶段：优化与测试（P2）
- 单元测试
- 集成测试
- 性能优化
- 文档完善

## 任务依赖关系图
用文本描述任务之间的依赖关系，确保任务可以按顺序执行。

---

**篇幅要求**：
- 全文目标 5000-7000 中文字符，任务覆盖完整优先
- 包含 15-25 个任务，完整覆盖 PRD 的各功能模块
- 每个任务都要有详细描述和 2-4 条具体可验证的验收标准

**输出要求**：
- 严格按照指定结构输出，使用 Markdown 格式
- 任务编号使用 T-01, T-02 格式
- 任务之间必须有合理的依赖关系
- 验收标准必须具体可验证
- 内容需贴合${domainLabel}领域特点
`;
}
